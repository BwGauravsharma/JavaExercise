package avinashpkg2;

public class AccessSpecifeBase {

	public void publicDisplay() {
		System.out.println("Public display method");
	}

	private void privateDisplay() {
		System.out.println("Private display method");
	}

	protected void protectedDisplay() {
		System.out.println("Protected display method");
	}

	void defaultDisplay() {
		System.out.println("default display method");
	}

}
